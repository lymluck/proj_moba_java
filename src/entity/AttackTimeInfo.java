package entity;

public class AttackTimeInfo {
    private int HeroAndTowerTime;
    private int TowerTime;

    public int getHeroAndTowerTime() {
        return HeroAndTowerTime;
    }

    public void setHeroAndTowerTime(int heroAndTowerTime) {
        HeroAndTowerTime = heroAndTowerTime;
    }

    public int getTowerTime() {
        return TowerTime;
    }

    public void setTowerTime(int towerTime) {
        TowerTime = towerTime;
    }
}
